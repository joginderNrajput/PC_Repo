#include <iostream>
using namespace std;

int main()
{
    cout << "Size of char datatype is : " << sizeof(char) << endl;
    cout << "Size of int datatype is : " << sizeof(int) << endl;
    cout << "Size of short int datatype is : " << sizeof(short int) << endl;
    cout << "Size of long int datatype is : " << sizeof(long int) << endl;
    cout << "Size of float datatype is : " << sizeof(float) << endl;
    cout << "Size of double datatype is : " << sizeof(double) << endl;
    cout << "Size of wchar_t datatype is : " << sizeof(wchar_t) << endl;
    return 0;
}